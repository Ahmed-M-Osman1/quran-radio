function player_type($type, $rands, $width, $height, $fullscreen, $autostart, $edc_radio_id, $icons, $title="", $showtitle=1){
	global $Radio_Languages;

	$code = '';
	if($type == 1){
		$code .= '<div style="width:100%; margin:5px 0 5px 0;">'."\n";
		$code .= '<div id="radio_container_post_'.$rands.'">Loading the player...</div>
		<script type="text/javascript">
			jwplayer("radio_container_post_'.$rands.'").setup({
				file: "'.$Radio_Languages[$edc_radio_id]['radio'].'/;*.mp3",
				autostart: '.$autostart.',
				type: "audio/mpeg",
				width: "100%",
				height: '.$height.',
				title: "'.$Radio_Languages[$edc_radio_id]['title'].'",
				controls: true,
			});
		</script>';
		$code .= '</div>';
	}elseif($type == 2){
		$code .= '<object id="mediaPlayer" width="'.$width.'" height="'.$height.'" classid="CLSID:22d6f312-b0f6-11d0-94ab-0080c74c7e95" codebase="http://activex.microsoft.com/activex/controls/ mplayer/en/nsmp2inf.cab#Version=5,1,52,701" standby="Loading Microsoft Windows Media Player components" type="application/x-oleobject">
		<param name="fileName" value="'.$Radio_Languages[$edc_radio_id]['asx'].'" />
		<param name="animationatStart" value="1" />
		<param name="enablecontextmenu" VALUE="0" />
		<param name="transparentatstart" value="0" />
		<param name="autoStart" value="'.$autostart.'" />
		<param name="showcontrols" value="1" />
		<param name="showdisplay" value="0" />
		<param name="showstatusbar" value="1" />
		<param name="loop" value="0" />
		<embed width="'.$width.'" height="'.$height.'" src="'.$Radio_Languages[$edc_radio_id]['asx'].'" type="application/x-mplayer2" pluginspage="http://microsoft.com/windows/mediaplayer/ en/download/" id="mediaPlayer" name="mediaPlayer" displaysize="4" autosize="0" EnableContextMenu="0" bgcolor="darkblue" showcontrols="1" showtracker="1" showdisplay="0" showstatusbar="1" videoborder3d="0" designtimesp="5311"></embed>
		</object>';
	}else{
		if($autostart == "true"){
			$autoplay = ' autoplay="autoplay"';
		}else{
			$autoplay = '';
		}
		$code .= '<div style="width:100%; margin:5px 0 5px 0;">'."\n";
		$code .= '<audio controls="controls"'.$autoplay.' src="'.$Radio_Languages[$edc_radio_id]['radio'].'/;*.mp3"></audio>';
		$code .= '</div>';
	}
	
	if($showtitle == 1){
		$code .= '<div style="padding:7px 0 7px 0; text-align:center;">'.$Radio_Languages[$edc_radio_id]['title'].''.$icons.'</div>';
	}else{
		if(!empty($icons)){
			$code .= '<div style="padding:7px 0 7px 0; text-align:center;">'.$icons.'</div>';
		}
	}
return $code;
}

class Quran_Radio_Widget extends WP_Widget {

	function __construct() {
		parent::__construct(
			'Quran_Radio_Widget', // Base ID
			__('Quran Radio', 'edc_radio_widget'), // Name
			array( 'description' => __( 'You can play 1 out of 40 translations of the Quran', 'edc_radio_widget' ), ) // Args
		);
	}

	public function widget( $args, $instance ) {
		global $Radio_Languages;

		$title = apply_filters( 'widget_title', $instance['title'] );
		$edc_radio_id = $instance['edc_radio_id'];
		$edc_radio_width = $instance['edc_radio_width'];
		$edc_radio_height = $instance['edc_radio_height'];
		$edc_radio_autostart = $instance['edc_radio_autostart'];
		$edc_radio_allowfullscreen = $instance['edc_radio_allowfullscreen'];
		$edc_icon_source = $instance['edc_icon_source'];
		$edc_icon_book = $instance['edc_icon_book'];
		$edc_icon_podcast = $instance['edc_icon_podcast'];
		$edc_icon_download = $instance['edc_icon_download'];
		$edc_player_type = $instance['edc_player_type'];
		$edc_show_title = $instance['edc_show_title'];

		if($edc_radio_autostart == 1){ $autostart = "true"; }else{ $autostart = "false"; }
		if($edc_radio_allowfullscreen == 1){ $fullscreen = "true"; }else{ $fullscreen = "false"; }
		if(empty($edc_radio_width)){ $width = "100%"; }else{ $width = $edc_radio_width; }
		if(empty($edc_radio_height)){ $height = 20; }else{ $height = $edc_radio_height; }

		$rands = rand(0,999);
		$languagescount = count($Radio_Languages);
		if($edc_radio_id > $languagescount){
			$code = '<p style="border:1px solid #cccccc; text-align:center; padding:10px;">Error ID!</p>';
		}else{
			$icons = '';
			if($edc_icon_source == 1){
				if(isset($Radio_Languages[$edc_radio_id]['url'])){ $icons .= '<a target="_blank" href="'.$Radio_Languages[$edc_radio_id]['url'].'"><img src="'.plugin_dir_url( __FILE__ ).'/i/url.png" alt="Source" title="Source" /></a>'; }
			}
			if($edc_icon_book == 1){
				if(isset($Radio_Languages[$edc_radio_id]['book'])){ $icons .= '<a target="_blank" href="'.$Radio_Languages[$edc_radio_id]['book'].'"><img src="'.plugin_dir_url( __FILE__ ).'/i/pdf.png" alt="PDF Book" title="PDF Book" /></a>'; }
			}
			if($edc_icon_podcast == 1){
				if(isset($Radio_Languages[$edc_radio_id]['podcast'])){ $icons .= '<a target="_blank" href="'.$Radio_Languages[$edc_radio_id]['podcast'].'"><img src="'.plugin_dir_url( __FILE__ ).'/i/podcast.png" alt="Podcast" title="Podcast" /></a>'; }
			}
			if($edc_icon_download == 1){
				if(isset($Radio_Languages[$edc_radio_id]['txt'])){ $icons .= '<a target="_blank" href="'.$Radio_Languages[$edc_radio_id]['txt'].'"><img src="'.plugin_dir_url( __FILE__ ).'/i/alllinks.png" alt="Download all files" title="Download all files" /></a>'; }
			}
			if($icons == ""){
				$get_icons = '';
			}else{
				$get_icons = '<div class="playericons">'.$icons.'</div>';
			}

			$code = '<div id="quran-radio-widget">';
			$code .= player_type($edc_player_type, $rands, $width, $height, $fullscreen, $autostart, $edc_radio_id, $get_icons, $title, $edc_show_title);
			$code .= '</div>';
		}

		echo $args['before_widget'];
		if ( ! empty( $title ) )
			echo $args['before_title'] . $title . $args['after_title'];
		echo __( ''.$code.'', 'edc_radio_widget' );
		echo $args['after_widget'];
	}

